    public void sum() {
        double sum = 0;
        for (int i = 0; i < mt.getRowCount() - 1; i++) {
            sum += Double.parseDouble(jTable1.getValueAt(i, 3).toString());
        }
        jTable1.setValueAt(sum, jTable1.getRowCount() - 1, 3);
    }

