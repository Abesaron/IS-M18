    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String a =jTextField1.getText();
        ArrayList<DataGames> TG;
         ReadDataGames dataG = new ReadDataGames();
        ArrayList<DataGames> myDG = dataG.ReadData();
        if(  a.isEmpty()
                 ) jTextField2.setText("Введены не все данные");
           else {
               if (((TG=searchTGames(a,myDG))==null) ||
                      TG.isEmpty() )
                      
                    {
                       tablStat= new JTabAllGames(TG);
                        jTable1.setModel(tablStat);
                        jTextField2.setText("Такого тура нет");
                     }
               else {   tablStat= new JTabAllGames(TG);
                        jTable1.setModel(tablStat);
                        jTextField2.setText("Данные введены");
                   
               }
           }
        
    }//GEN-LAST:event_jButton1ActionPerformed

