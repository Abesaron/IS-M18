public ArrayList<DataGames> searchTGames(String x, ArrayList<DataGames> y){
        if(y==null) return null;
        ArrayList<DataGames> resuname = new ArrayList<>();
       
        
        for(int i=0; i<y.size(); i++){
            if( x.equalsIgnoreCase(y.get(i).getTour()) )
            {       resuname.add(y.get(i)); 
            
                   
            } 
        } 
        return resuname;
        
        
        
        
    }
public ArrayList<DataGames> searchDateGames(String x, ArrayList<DataGames> y){
        if(y==null) return null;
        ArrayList<DataGames> resuname = new ArrayList<>();
       
        
        for(int i=0; i<y.size(); i++){
            if( x.equalsIgnoreCase(y.get(i).getDate()) )
            {       resuname.add(y.get(i)); 
            
                   
            } 
        } 
        return resuname;
        
        
        
        
    }
